#pragma once

#include "core.h"

////////////////////////////////////////////////////////////////////////////////////////////////////

struct size2i // [0...x), [0...y)
{
	i64 x = 0;
	i64 y = 0;

	bool empty()  const { return (x <= 0) || (y <= 0); }
	i64  square() const { return empty() ? 0 : x * y; }
};

////////////////////////////////////////////////////////////////////////////////////////////////////

struct rangei // [...)
{
	i64 min = 0;
	i64 max = 0;
};

////////////////////////////////////////////////////////////////////////////////////////////////////

struct rect2i
{
	rangei x, y; // [...) [...)

	rect2i() = default;
	rect2i(size2i size) : x{ 0LL, size.x }, y{ 0LL, size.y } {}
};
